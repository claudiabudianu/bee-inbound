<h5 class="portfolio_title">Bankbewertung als Grundlage für Kapitalerhöhung</h5>
</div>
<div class="ifbc-sc-portfolio-btn-open"><span class="icon_holder q_font_elegant_icon arrow_carrot-down" aria-hidden="true"></span></div>
<p>
<p>Im Rahmen einer Aktienkapitalerhöhung hat der Verwaltungsrat der Clientis EB Entlebucher Bank IFBC beauftragt, einen angemessenen Marktwert für das Eigenkapital zu quantifizieren. Zu diesem Zweck wurde ein Bewertungsmodell entwickelt, welches die spezifischen Werttreiber der Regionalbank berücksichtigt. Basierend auf verschiedenen Szenarien wurden eine Wertbandbreite und ein fairer Wert pro Aktie als Entscheidungsgrundlage für die Preisbestimmung quantifiziert. Zudem wurde anhand der Szenarien der potenzielle Kapitalbedarf für die Zukunft aufgezeigt.</p>
<p>Die Clientis EB Entlebucher Bank ist eine von 15 Retailbanken im Clientis Verbund.</p>
