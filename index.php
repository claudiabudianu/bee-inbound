<p>IFBC entwickelte im Auftrag von Emmi ein standardisiertes Modell für Projektbewertungen. Dabei wurde das Modell auf das spezifische Geschäftsmodell von Emmi ausgerichtet mit Fokus auf die wesentlichen Werttreiber. Zudem kann im Modell zwischen Wachstums-, Rationalisierung und Ersatzinvestitionen unterschieden werden. Die Modellstruktur sowie die bewertungstechnischen Parameter passen sich entsprechend an. Durch die standardisierte Berechnungsweise, die konsequente Ausrichtung auf die wichtigsten Werttreiber sowie die transparente Darstellung der Ergebnisse basierend auf verschiedenen Szenarien dient das Modell als optimale Entscheidungsunter-<br/>
stützung.</p>
<p>Emmi ist die grösste Schweizer Milchverarbeiterin und eine der innovativsten Premium-Molkereien in Europa.</p>
