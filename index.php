<p>IFBC wurde von fenaco beauftragt, den Minimalgewinn der UFA AG (Tochterunternehmen der fenaco) unter Berücksichtigung betriebswirtschaftlicher Grundsätze zu bestimmen und den effektiven betriebswirtschaftlichen Gewinn der UFA AG zu beurteilen. Darüber hinaus hat IFBC die Mischfutterpreise der UFA AG mit Konkurrenten innerhalb der Schweiz sowie mit Konkurrenten im nahen Ausland verglichen und beurteilt.</p>
