<p>
<p>Im Rahmen der Beurteilung potentieller Übernahmekandidaten im Ingenieurs- und Planungswesen wurde IFBC von der BKW mit der Durchführung der jeweiligen Financial Due Diligence beauftragt. Die Erkenntnisse aus den Financial Due Diligences wurden jeweils in einem standardisierten Bericht zusammengefasst. Die Berichte dienten der BKW als Entscheidungsgrundlage zur Beurteilung der Transaktion sowie für die Preisfindung.</p>
<p>Die BKW Gruppe ist ein führendes Energieversorgungsunternehmen in der Schweiz.</p>
